/**
 * Translate a scholarship name to the given language
 */
export function translateScholarshipName(name, lang) {
  if (!lang || lang === 'en') return name;
  return scholarshipTranslations[lang]?.[name] || name;
}

/**
 * Translate a scholarship description using term replacement
 */
export function translateDescription(description, lang) {
  if (!lang || lang === 'en' || !description) return description;
  const terms = descriptionTerms[lang];
  if (!terms) return description;
  
  let translated = description;
  // Sort by length descending to replace longer phrases first
  const sortedTerms = Object.entries(terms).sort((a, b) => b[0].length - a[0].length);
  for (const [en, localized] of sortedTerms) {
    const regex = new RegExp(`\\b${en}\\b`, 'gi');
    translated = translated.replace(regex, localized);
  }
  return translated;
}

